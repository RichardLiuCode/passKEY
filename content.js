function binaryToText(binaryStr) {
  return binaryStr
    .split(' ')                        // 先用空格分割每個 8 位元
    .map(bin => String.fromCharCode(parseInt(bin, 2)))  // 將二進制轉成十進位，然後轉成字元
    .join('');                         // 合併所有字元變成一句話
}


